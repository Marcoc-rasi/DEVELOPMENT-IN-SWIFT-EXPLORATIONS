## Multiple Choice
 
 You’re often given choices to make in life. In many cases, you must pick one of a set of options, and picking nothing isn’t a choice.
 
 For example, a restaurant might offer a limited lunch menu with three options for entrées: pasta, a burger, or soup.
 
 This system works well:
 
- The restaurant's manager knows exactly which ingredients need to be on hand every day for lunch, so they can order supplies well ahead of time.
- The kitchen can prepare and serve each meal quickly and efficiently.
- No time is wasted understanding or taking down special orders.
 
 As a programmer you can benefit from applying these same kinds of controlled choices in your programs.
 
