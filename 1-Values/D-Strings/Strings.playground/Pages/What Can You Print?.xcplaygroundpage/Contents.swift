/*:
## What Can You Print?
 
 Anything that you’d see in the results sidebar, you can `print` to the console.
 
 Open the results sidebar and confirm this for yourself by checking the examples.
  
 You can print strings:
 */
"Hello, world!"
print("Hello, world!")
//: Numbers and calculations:
8
print(8)
7 + 11
print(7 + 11)
//: String expressions:
"Have you seen" + "..." + "nevermind."
print("Have you seen" + "..." + "nevermind.")
//: Constants:
let authorName = "Beatrix Potter"
print(authorName)
let bookTitle = "Jemima Puddleduck"
print(bookTitle)
//: Variables (you'll learn about these in the next lesson):
var favoriteCookie = "chocolate chip"
print("My favorite type of cookie is \(favoriteCookie).")
favoriteCookie = "double chocolate chip"
print("Wait! I forgot about \(favoriteCookie). That one is even better.")
/*:
 The result of whatever’s inside the parentheses will appear in the console.

 - Experiment: Look for the following statements in the results sidebar, then make each statement print to the console, following the example.\
Before:\
`authorName`\
After:\
`print(authorName)`
 */
print("\(authorName)")
print("authorName")
print("Did you know that \(bookTitle) was written by \(authorName)?")
