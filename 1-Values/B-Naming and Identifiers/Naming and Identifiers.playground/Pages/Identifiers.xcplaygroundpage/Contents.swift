## Identifiers
In Swift, a name is formally called an _identifier_, and you'll see the term _identifier_ used in error messages. But programmers typically use the more informal word _name_. In this course, you'll become familiar with both terms.

Names for constants start with a lowercase letter. As in the examples below, they can include several words, all run together. (If there's a space between the words, Swift will think each word is a different identifier.)

To make it easier to read identifiers with multiple words, every word after the first word should be capitalized. This way of writing out names is called _camel case_, because the capital letters in the middle of the name look a bit like the humps of a camel. üê´
